import UIKit

extension UIFont {
    
    static func avenirBold18() -> UIFont? {
        return UIFont(name: "Avenir Next Demi Bold", size: 18)
    }
    
    static func avenirBold14() -> UIFont? {
        return UIFont(name: "Avenir Next Demi Bold", size: 14)
    }
    
    static func avenirNext18() -> UIFont? {
        return UIFont(name: "Avenir Next", size: 18)
    }
    
    static func avenirNext14() -> UIFont? {
        return UIFont(name: "Avenir Next", size: 14)
    }
}
